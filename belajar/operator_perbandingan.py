# Operasi Perbandingan


print("\n--- Operasi Perbandingan ---")
print(f"10 > 5: {10 > 5}")   # Lebih besar dari
print(f"10 < 5: {10 < 5}")   # Lebih kecil dari
print(f"10 == 10: {10 == 10}")  # Sama dengan
print(f"10 != 5: {10 != 5}")   # Tidak sama dengan
print(f"10 >= 10: {10 >= 10}")  # Lebih besar atau sama dengan
print(f"5 <= 10: {5 <= 10}")   # Lebih kecil atau sama dengan
