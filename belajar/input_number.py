print("Angka Pertama")
a = int(input("Masukkan angka pertama: "))
print("Angka Kedua")
b = int(input("Masukkan angka kedua: "))
print("Hasil Penjumlahan")
print(f"{a} + {b} = {a + b}")
print("Hasil Pengurangan")
print(f"{a} - {b} = {a - b}")
print("Hasil Perkalian")
print(f"{a} * {b} = {a * b}")
print("Hasil Pembagian")
print(f"{a} / {b} = {a / b}")